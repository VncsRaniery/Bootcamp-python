frutas = ("maçã", "laranja", "uva", "pera",)

print(frutas[0])  # maçã
print(frutas[2])  # uva

# Explicando em detalhes: O acesso direto a um elemento de uma tupla é feito da mesma forma que em uma lista, através de um índice que representa a posição do elemento na tupla. No exemplo acima, a tupla frutas contém quatro elementos: maçã, laranja, uva e pera. Portanto, frutas[0] retorna o primeiro elemento da tupla, que é maçã, e frutas[2] retorna o terceiro elemento da tupla, que é uva.