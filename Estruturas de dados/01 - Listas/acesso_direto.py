frutas = ["maçã", "laranja", "uva", "pera"]

print(frutas[0])  # maçã
print(frutas[2])  # uva

# Explicando em detalhes: A lista frutas contém quatro elementos: maçã, laranja, uva e pera. O acesso direto a um elemento de uma lista é feito através de um índice, que é um número inteiro que representa a posição do elemento na lista. O índice começa em 0, ou seja, o primeiro elemento da lista tem índice 0, o segundo elemento tem índice 1, e assim por diante. Portanto, frutas[0] retorna o primeiro elemento da lista, que é maçã, e frutas[2] retorna o terceiro elemento da lista, que é uva.