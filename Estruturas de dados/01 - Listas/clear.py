lista = [1, "Python", [40, 30, 20]]

print(lista)  # [1, "Python", [40, 30, 20]]

lista.clear()

print(lista)  # []

# Explicando detalhadamente: O método clear() é utilizado para remover todos os elementos de uma lista, deixando-a vazia. Neste exemplo, a lista é inicializada com três elementos e, em seguida, o método clear() é chamado para remover todos os elementos. Após a chamada do método, a lista fica vazia, como mostrado no segundo print.