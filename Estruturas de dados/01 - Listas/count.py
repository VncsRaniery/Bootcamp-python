cores = ["vermelho", "azul", "verde", "azul"]

print(cores.count("vermelho"))  # 1
print(cores.count("azul"))  # 2
print(cores.count("verde"))  # 1

# Explicando detalhadamente: O método count() é utilizado para contar o número de ocorrências de um determinado elemento em uma lista. Neste exemplo, a lista cores contém quatro elementos: "vermelho", "azul", "verde" e "azul". O método count() é chamado para contar o número de ocorrências de cada cor na lista. O resultado é impresso na tela, mostrando que "vermelho" ocorre uma vez, "azul" ocorre duas vezes e "verde" ocorre uma vez.