# Explicando com detalhes: Temos uma lista chamada frutas que contém três elementos: laranja, maçã e uva. Em seguida, a lista é redefinida para uma lista vazia. Depois, a lista letras é criada a partir da string "python". A função list() cria uma lista a partir de um objeto iterável. Por fim, a lista números é criada a partir da função range(), que gera uma sequência de números inteiros. A lista carro contém informações sobre um carro, como marca, modelo, preço, ano, potência, cidade, etc. Note que uma lista pode conter elementos de diferentes tipos. 