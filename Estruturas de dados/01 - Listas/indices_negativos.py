frutas = ["maçã", "laranja", "uva", "pera"]

print(frutas[-1])  # pera
print(frutas[-3])  # laranja

# Explicando em detalhes: O acesso a elementos de uma lista pode ser feito utilizando índices negativos. Nesse caso, o índice -1 refere-se ao último elemento da lista, o índice -2 refere-se ao penúltimo elemento, e assim por diante. Portanto, frutas[-1] retorna o último elemento da lista, que é pera, e frutas[-3] retorna o terceiro elemento da lista, que é laranja.