lista = []

lista.append(1)
lista.append("Python")
lista.append([40, 30, 20])

print(lista)  # [1, "Python", [40, 30, 20]]

# Explicando detalhadamente: O método append() é utilizado para adicionar um elemento ao final de uma lista. Neste exemplo, a lista é inicializada vazia e, em seguida, são adicionados três elementos: o número 1, a string "Python" e uma lista [40, 30, 20]. Ao final, a lista resultante é [1, "Python", [40, 30, 20]].