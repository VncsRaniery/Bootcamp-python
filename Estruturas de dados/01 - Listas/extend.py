linguagens = ['Python', 'Java', 'C#']

print(linguagens) # => ['Python', 'Java', 'C#']

linguagens.extend(['JavaScript', 'Ruby'])

print(linguagens) # => ['Python', 'Java', 'C#', 'JavaScript', 'Ruby']

# A função extend() adiciona os elementos de uma lista ao final de outra lista. No exemplo acima, adicionamos os elementos ['JavaScript', 'Ruby'] à lista linguagens.