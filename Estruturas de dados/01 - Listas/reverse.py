linguagens = ["python", "js", "c", "java", "csharp"]

linguagens.reverse()

print(linguagens)  # ["csharp", "java", "c", "js", "python"]

# Explicando em detalhes: O método reverse() inverte a ordem dos elementos da lista. Ou seja, o primeiro elemento passa a ser o último, o segundo elemento passa a ser o penúltimo, e assim por diante. Após a execução do método, a lista é modificada permanentemente.