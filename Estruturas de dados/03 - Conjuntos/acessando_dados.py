numeros = {1, 2, 3, 2}

numeros = list(numeros)

print(numeros[0])

# Explicando detalhadamente: Conjuntos não possuem índices, pois são coleções não ordenadas. No entanto, é possível converter um conjunto em uma lista para acessar os elementos por índice. Neste exemplo, o conjunto números contém os elementos 1, 2 e 3. Ao converter o conjunto em uma lista, o primeiro elemento (índice 0) é impresso na tela, resultando em 1.