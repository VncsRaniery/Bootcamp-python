sorteio = {1, 23}

print(sorteio)  # {1,23}

sorteio.clear()

print(sorteio)  # {}

# Explicando detalhes: O método clear() é utilizado para remover todos os elementos de um conjunto, deixando-o vazio. Neste exemplo, o conjunto sorteio contém os elementos 1 e 23. Ao chamar o método clear(), todos os elementos são removidos, resultando em um conjunto vazio.