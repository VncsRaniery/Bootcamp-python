conjunto_a = {1, 2}
conjunto_b = {3, 4}

resultado = conjunto_a.union(conjunto_b)
print(resultado)

# Explicando detalhes: O método union() retorna a união de dois conjuntos, ou seja, um novo conjunto contendo todos os elementos dos conjuntos originais. Neste exemplo, o conjunto resultado contém os elementos 1, 2, 3 e 4, que são a união dos conjuntos A e B. O método union() não modifica os conjuntos originais, apenas retorna um novo conjunto com a união dos elementos.