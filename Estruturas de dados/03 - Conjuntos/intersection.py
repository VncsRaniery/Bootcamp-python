conjunto_a = {1, 2, 3}
conjunto_b = {2, 3, 4}

resultado = conjunto_a.intersection(conjunto_b)
print(resultado)

# Explicando detalhes: O método intersection() retorna a interseção de dois conjuntos, ou seja, um novo conjunto contendo apenas os elementos que estão presentes em ambos os conjuntos originais. Neste exemplo, o conjunto resultado contém os elementos 2 e 3, que são a interseção dos conjuntos A e B. O método intersection() não modifica os conjuntos originais, apenas retorna um novo conjunto com a interseção dos elementos.